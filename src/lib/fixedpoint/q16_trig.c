int
sin_q16(uint16_t angle)
{
  uint16_t t;
  if(angle & 0x4000)
    t = 0x3fff - (angle & 0x3fff);
  else
    t = angle & 0x3fff;

  const uint16_t w = t & SIN_TABLE_WMASK;

  const uint16_t x = t >> (14 - SIN_TABLE_SHIFT);

  const int a = sintable[x];
  const int b = sintable[x + 1];

  const int r = (((b - a) * w) >> SIN_WEIGHT_SHIFT) + a;

  if(angle & 0x8000)
    return -r;
  return r;
}

int
cos_q16(uint16_t angle)
{
  return sin_q16(angle + 0x4000);
}
