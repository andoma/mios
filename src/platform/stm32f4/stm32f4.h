#pragma once

#define CORTEXM_IRQ_COUNT 96

#define STM32F4_DEVID_05 0x413 // STM32F405xx/07xx and STM32F415xx/17xx
#define STM32F4_DEVID_42 0x419 // STM32F42xxx and STM32F43xxx
#define STM32F4_DEVID_11 0x431 // STM32F411
#define STM32F4_DEVID_46 0x421 // STM32F446

#define STM32F4_BIT_05 0
#define STM32F4_BIT_42 1
#define STM32F4_BIT_11 2
#define STM32F4_BIT_46 3

#define STM32F4_05 (1 << STM32F4_BIT_05)
#define STM32F4_42 (1 << STM32F4_BIT_42)
#define STM32F4_11 (1 << STM32F4_BIT_11)
#define STM32F4_46 (1 << STM32F4_BIT_46)
